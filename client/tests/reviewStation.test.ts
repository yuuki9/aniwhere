  assert.match(station, /existingReviewImages\.length > 0/)
  assert.match(station, /<header className="map-review-shop-summary">/)
  assert.doesNotMatch(station, /shopCategoryLabel/)
  assert.match(station, /const submitLabel = isEditing \? '수정 완료' : '작성 완료'/)
