  const supplement = supplementSource()
  const station = reviewStationSource()
  const tdsFacade = tdsFacadeSource()
  const styles = stylesSource()

  assert.match(explore, /import \{ createShopReview, deleteShopReview, listShopReviews, updateShopReview \} from '\.\.\/shared\/api\/shopReviews'/)
