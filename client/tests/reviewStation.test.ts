  assert.match(station, /onChange=\{\(event\) => handleContentChange\(event\.target\.value\)\}/)
  assert.match(station, /const canSubmit =[\s\S]*?rating > 0 && normalizedContent\.length > 0/)
  assert.match(station, /normalizedContent\.length < MIN_REVIEW_CONTENT_LENGTH/)
