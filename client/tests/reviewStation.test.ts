  assert.match(station, /MAX_REVIEW_IMAGES = 5/)
  assert.match(station, /const handleRatingChange = \(nextRating: number\) => \{[\s\S]*?setLocalError\(null\)/)
  assert.match(station, /const handleContentChange = \(nextContent: string\) => \{[\s\S]*?setLocalError\(null\)/)
